								<p>Termenul "Destrămarea Iugoslaviei" reprezintă procesul prin care toate republicile din Republica Socialistă Federativă Iugoslavia s-au desprins de Patria Mamă. Acest proces a dus la o serie de răsturnari politice și conflicte, iar în cele din urmă la dispariția federației de pe harta Europei. R.S.F. Iugoslavia a fost o țară care ocupa o fâșie de pamânt care se întindea din Europa Centrală până în Balcani - o regiune cu o istorie de conflicte etnice.</p>
								<p> Aceasta țară a fost o conglomerare de 6 republici regionale și două provincii autonome, care au fost dur împărțite pe linii etnice și împărțite în anii 1990 în mai multe țări independente. Aceste opt unități federale au fost cele șase republici: Slovenia, Croația, Bosnia și Herțegovina, Muntenegru, Serbia, Macedonia și cele două provincii autonome ale Serbiei: Kosovo și Voivodina. Având o compoziție etnica mixtă, Bosnia și Herțegovina nu a existat ca stat din 1465, și în plus, nu avea un grup etnic majoritar cu 44% musulmani, 33% sârbi, 18% croați și minorități. Repartizarea geografică a grupurilor etnice, din care a fost compusă Iugoslavia, a fost astfel încât fiecare națiune să aibă o populație în care trăiesc în toate cele șase republici. Iugoslavia a fost declarat a fi fost o țară cu șapte frontiere, șase republici, cinci naționalități, patru limbi, trei religii, două alfabete, și un sef.</p>
								<p>Conflictul politic a început în anul 1990, imediat după proclamarea independenței așa-zisei „Republicii Moldovenești Nistrene”, fază latentă a acestuia desfășurându-se până în prezent. În ultima parte a anilor 1980, peisajul politic al URSS era în plină schimbare datorită politicii de perestroika întreprinse de Mihail Gorbaciov, care permitea liberalizarea politică la nivel regional. Democratizarea incompletă a permis naționalismului exclusivist să devină cea mai dinamică doctrină politică. Unele minorități naționale s-au opus schimbărilor de clasă politică din Republica Moldova, clasă dominată în perioada sovietică de etnicii ruși. Oficializarea limbii majorității [11] și introducerea obligativității alfabetului latin pentru scrierea acesteia a atras proteste din partea vorbitorilor de alte limbi decât cea română.[11]
Problematica limbilor oficiale din Republica Moldova a devenit foarte spinoasă și a fost, probabil, intenționat politizată. Neconcordonața cu noua politică s-a manifestat într-un mod mai vizibil în Transnistria, regiune în care etnicii slavi (ruși sau ucraineni) erau majoritari în zonele urbane. Protestele față de guvernul republican erau mai puternice aici. La recensământul din 1989, în Transnistria locuiau 40,0% moldoveni, 28,3% ucraineni, 25,4% ruși și 1,9% bulgari.
</p>
